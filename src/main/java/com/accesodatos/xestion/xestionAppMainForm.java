    //Cargar provincias España
    private void LoadProvinces() {
        // jsonToObj devuelvo un Object, para pasarlo a nuestra clase tenemos que hacer así
        ManageJson mj = new ManageJson();
        provincias = Provincias.class.cast(mj.jsonToObj(pathProvincias, Provincias.class));
        System.err.println("prueba contenido provinces");
        LinkedList<Province> listProvincias = provincias.getProvincias();
        Iterator it = listProvincias.iterator();

        String db = "xestionAppDB.db";
        Connection con = null;
        con = DB.connectDatabase(db);

        while (it.hasNext()) {
            Province tempProvince = (Province) it.next();
            System.out.println("provincia: " + tempProvince.toString());
            DB.insertProvince(con, Integer.valueOf(tempProvince.getId()), tempProvince.getNome());
        }
        System.err.println(provincias.toString());
        DB.desconnetDatabase(con);
    }
