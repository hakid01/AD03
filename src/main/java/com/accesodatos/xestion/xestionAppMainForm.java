    public xestionAppMainForm() {
        initComponents();
        this.setLocationRelativeTo(null); //Abrir app en el centro de la pantalla

        //Para solo comprobar la db al inicio de la app
        if (!dbComprobada) {

            String db = "xestionAppDB.db";
            File dbFile = new File(db);
            Connection con = null;
            System.out.println("dbFile: " + dbFile.getAbsolutePath());
            //Creación base de datos si no existe
            if (!dbFile.exists()) {
                DB.createDatabase(db);
            }

            con = DB.connectDatabase(db);

            /*Creación de tablas (en el código de cada tabla 
            se especifica que se creee si no existe)*/
            DB.createTable(con, DB.getSqlCustomers());
            DB.createTable(con, DB.getSqlEmployees());
            DB.createTable(con, DB.getSqlItems());
            DB.createTable(con, DB.getSqlProvinces());
            DB.createTable(con, DB.getSqlStores());
            DB.createTable(con, DB.getSqlStockItemStore());
            DB.createTable(con, DB.getSqlHoursEmployeeStore());

            DB.desconnetDatabase(con);

            dbComprobada = true;
        }

        //Insertamos las provincias de España
        LoadProvinces();

    }
    
