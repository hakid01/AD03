    private void btnLoadBackupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadBackupActionPerformed
        // Mostrar cuadro dialogo de archivos para elegir uno
        JFileChooser selectFile = new JFileChooser();
        selectFile.setFileSelectionMode(JFileChooser.FILES_ONLY); //solo seleccionar archivos, no carpetas/directorios

        selectFile.showOpenDialog(this);

        File file = selectFile.getSelectedFile();
        if (file != null) {
            txtPathJsonBackUp.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_btnLoadBackupActionPerformed

